<b>TRANSPORTATION SAFETY TIPS:</b><br/>
While traveling you should always keep in mind certain transportation safety tips as well:-
 Always choose the safest mode of transport for traveling. Do check its previous safety track record.<br/>
Be mindful of other passengers traveling with you and never hesitate to report any suspicious act to the concerned authorities.<br/>
 Never share cab or taxi with strangers. As carjacking is the biggest threat for a traveler.<br/>
  While traveling by flight, always pay attention to the preflight briefing about closest emergency exit door, fastening your seat belts properly, applying oxygen masks and other important details to be followed in case of emergency.<br/>
    Do not ignore all significant details provided by flight attendant. They are prime source of aid in case of emergency and unexpected turbulence.<br/>
    Wear money belts, hidden neck and waist pouches, hidden pocket wallets and zippered compartments under your clothes to avoid pick pocketing.<br/><br/>

<b>HOTEL SAFETY TIPS: </b><br/><br/>

