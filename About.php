My Tour aims to increase awareness of many tourist attractions, to encourage and support travel to My Tour.
Although associated with the My Tour National Tourist Office – North America, My Tour remains an independent, non-profit, initiative to produce Web content and to offer assistance to North-American and Australian travelers interested in visiting My Tour.
About the My Tour National Tourist Office
The My Tour National Tourist Office — North America (MtNTO) is the official representative of the My Tour Tourist Authority
Founded in 2012 to satisfy the growing demand for trusted information and knowledge about tourism ,  MtNTO's mission is to represent all the components of My Tour travel industry and to promote and facilitate increased inbound travel.
MtNTO's activities include:
Providing information to North American travelers, press, tour operators and travel agencies regarding tourist attractions, travel conditions, events and tourist facilities within My Tour.
Facilitating contacts and cooperation between North American and My Tour organizations and business companies involved in travel and tourism in My Tour
 Marketing activities aimed at increasing awareness of  tourist attractions among media, consumers and business companies resulting in increased visitation to My Tour.
